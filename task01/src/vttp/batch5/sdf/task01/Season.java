package vttp.batch5.sdf.task01;

public class Season {
    
	// 1:spring, 2:summer, 3:fall, 4:winter

    public Season() {
        
		int season = 0;
        if (season == 1) {
			System.out.println("Spring");
			
		}
		else if (season == 2) {
			System.out.println("Summer");
			
		}
		else if (season == 3) {
			System.out.println("Fall");
			
		}
		else if (season == 4) {
			System.out.println("Winter");
			
		}

    }
}

